void StartRTCTask(void *argument)
{
	while (1)
	{
		uint8_t message[50] = "RTC task begin\n";
		HAL_UART_Transmit(&huart1, message, strlen((char*)message), HAL_MAX_DELAY);
		GetTime(&DateTimeHandle);

		if (xQueueSend(DateTimeQueueHandle, &DateTimeHandle, portMAX_DELAY) == pdPASS)
		{
			uint8_t message[50] = "RTC sent successfully\n";
			HAL_UART_Transmit(&huart1, message, strlen((char*)message), HAL_MAX_DELAY);
		}
		vTaskDelay(pdMS_TO_TICKS(1000));
	}
}

void StartSSD1306Task(void *argument)
{
	while (1)
	{
		DateTime_HandleTypeDef dateTime;
		uint8_t message[50] = "SSD1306 task begin\n";
		HAL_UART_Transmit(&huart1, message, strlen((char*)message), HAL_MAX_DELAY);

		if (xQueueReceive(DateTimeQueueHandle, &dateTime, portMAX_DELAY) == pdPASS)
		{
			uint8_t time[50];
			uint8_t linebreak = '\n';
			sprintf((char*)time, "%02d:%02d:%02d", DateTimeHandle.dateTime.hour, DateTimeHandle.dateTime.minute, DateTimeHandle.dateTime.second);
			HAL_UART_Transmit(&huart1, time, strlen((char*)time), HAL_MAX_DELAY);
			HAL_UART_Transmit(&huart1, &linebreak, 1, HAL_MAX_DELAY);
			//ssd1306_ClearScreen();
			ssd1306_GoToPage(2);
			ssd1306_PageIndentedBy(2);
			ssd1306_PutString(time);
			sprintf((char*)time, "%02d/%02d/%02d", DateTimeHandle.dateTime.date, DateTimeHandle.dateTime.month, DateTimeHandle.dateTime.year);
			HAL_UART_Transmit(&huart1, time, strlen((char*)time), HAL_MAX_DELAY);
			HAL_UART_Transmit(&huart1, &linebreak, 1, HAL_MAX_DELAY);

			ssd1306_GoToPage(3);
			ssd1306_PageIndentedBy(2);
			ssd1306_PutString(time);
		}
		vTaskDelay(pdMS_TO_TICKS(500));
	}
}
